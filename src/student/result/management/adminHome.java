    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
      // TODO add your handling code here:
    String course = (String) jComboBox1.getSelectedItem();
    String branch = (String) jComboBox2.getSelectedItem();
    String rollNo = jTextField1.getText();
    String name = jTextField2.getText();
    String gender = (String) jComboBox5.getSelectedItem();
    String fatherName = jTextField3.getText();

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Cloudaerith76");

    // Using PreparedStatement to prevent SQL injection
    String sql = "INSERT INTO STUDENT(course, branch, rollNo, name, gender, fatherName) VALUES (?, ?, ?, ?, ?, ?)";
    try (PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setString(1, course);
        pst.setString(2, branch);
        pst.setString(3, rollNo);
        pst.setString(4, name);
        pst.setString(5, gender);
        pst.setString(6, fatherName);

        int rowsAffected = pst.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new adminHome().setVisible(true);
        }
    }
}       catch (ClassNotFoundException | SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
 }
    }//GEN-LAST:event_jButton6ActionPerformed

